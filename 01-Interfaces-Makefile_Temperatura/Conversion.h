#ifndef CONVERSION_H
#define CONVERSION_H

/*
En este archivo se definen los prototipos de las funciones Celsius y Fahrenheit.
Estas funciones se encargan de realizar la conversión de temperaturas de Celsius
a Fahrenheit y de Fahrenheit a Celsius.
La función Celsius recibe un valor en grados Fahrenheit y devuelve su equivalente
en grados Celsius.
La función Fahrenheit recibe un valor en grados Celsius y devuelve su equivalente
en grados Fahrenheit.
*/

double Celsius(double fahrenheit);
double Fahrenheit(double celsius);

#endif

